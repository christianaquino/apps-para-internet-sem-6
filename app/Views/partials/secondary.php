                <p>
                    El Índice Mensual de Actividad Económica (Imacec) de febrero mostró una contracción de la economía del 2,2%, muy por sobre las estimaciones que la posicionaban en un 1,6% promedio.
                    Expertos aseguraron que la cifra se explicaría por una baja base de comparación respecto a igual periodo en 2020, aunque anticiparon que el segundo trimestre de este año podría ser el periodo más complejo.
                    La economía se contrajo un 2,2% durante el segundo mes de este año, según los determinado por el Banco Central para el Índice Mensual de Actividad Económica. Con esto se registra una caída en la economía en 11 de los últimos 12 meses, con un repunte del 1% solo en noviembre pasado.
                    El Imacec explicó esta cifra por una caída en las actividades de Servicios, que cayeron un 5,3% afectando principalmente a restaurantes y hoteles, transporte y actividades culturales; y por otro lado a la Producción de Bienes, con una baja del 2,8%, afectada principalmente por el desempeño de la minería que disminuyó un 4,9%.
