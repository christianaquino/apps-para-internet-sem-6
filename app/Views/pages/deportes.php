            Tsitsipas mantuvo el pie en el acelerador en la segunda manga rompiendo el servicio del polaco en el primer juego y tomando una ventaja 2-1 que parecía encaminarle hacia un triunfo por la vía rápida.
            “En ese momento traté de seguir siendo competitivo”, recordó Hurkacz tras el partido. “Sabía que si empezaba a meter los puntos que estaba fallando podía darle la vuelta”.
            El polaco, de 24 años, mejoró su servicio y aprovechó los crecientes errores no forzados de Tsitsipas para tomar ventaja en el marcador y llevar el partido al desempate entre vehementes lamentos del griego.
            “No pude sacar provecho de mi servicio, que es lo más importante en ese momento. No tenía ni que quebrar, solo mantener el servicio”, lamentó Tsitsipas.
            En el descanso, el griego fue apercibido por el juez por tardar demasiado en cambiarse de cinta y camiseta pero, de vuelta a la pista, arrancó la última manga distanciándose de nuevo por 2-1.
            En el cuarto juego, sin embargo, el griego desperdició cuatro pelotas para ponerse 3-1 y cedió un quiebre del que ya no se recuperó.
            “Es una derrota muy decepcionante. Todo estaba bajo control y de repente hubo una autoexplosión”, resumió Tsitsipas.
