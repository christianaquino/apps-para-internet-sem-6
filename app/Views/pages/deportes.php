            Tsitsipas, número cinco del ránking ATP, era el mejor posicionado en Miami tras la derrota del ruso Daniil Medvedev, número dos mundial, en cuartos ante el español Roberto Bautista.
            La caída de Medvedev dejó al Abierto sin ningún campeón de un Masters 1000, en la primera edición en la que no compitieron los tres gigantes del tenis masculino (Federer, Nadal y Djokovic) desde 2004.
            Tsitsipas, que eliminó a Nadal en los cuartos del pasado Abierto de Australia, dejó escapar un set de ventaja ante un rival al que había ganado en seis de sus siete enfrentamientos anteriores, el último apenas el 4 de marzo en Rotterdam.
            El griego, de 22 años, perdió los nervios en varios momentos del partido y fue apercibido por el juez por pérdida de tiempo.
